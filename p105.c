#include <stdio.h>

int main()
{
    int n,a[100],i,t,j;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=0;i<n;i++)
    {
        for(j=i+1;j<n;j++)
        {
            if(a[i]>a[j])
            {
                t=a[i];
                a[i]=a[j];
                a[j]=t;
            }
        }
    }
    for(i=n-1;i>=0;i--)
    {
        printf("%d ",i+1);
    }

    return 0;
}
